from fastapi.middleware.cors import CORSMiddleware
from fastapi import FastAPI
from app.api.routers import api_router

app = FastAPI()

origins = [
    "http://localhost:8080",
]

app.add_middleware(
    CORSMiddleware,
    allow_origins=origins,
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"]
)

app.include_router(api_router, prefix='/api')

